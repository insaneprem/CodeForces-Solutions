void solve()
{
    int n;
    cin >> n;

    vector<int> arr(n);
    for (auto &i : arr)
        cin >> i;

    int a = 1e8, b = 1e8, ans = 0;

    for (auto i : arr)
    {
        if (a > b)
            swap(a, b);

        if (i <= a)
            a = i;
        else if (i <= b)
            b = i;
        else
            a = i, ans++;
    }

    cout << ans;
}
